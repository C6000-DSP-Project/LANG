#define HWREG(x)              (*((volatile unsigned int *)(x)))

#define SYSCFG_PINMUX13_REG   (0x01C14154)

// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//      管脚复用配置
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
void GPIOBankPinMuxSet()
{
    HWREG(SYSCFG_PINMUX13_REG) = (HWREG(SYSCFG_PINMUX13_REG) & ~(0xFu << 28)) | ((8u << 8) | (8u << 12));
}
